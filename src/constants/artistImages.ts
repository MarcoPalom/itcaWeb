// Función helper para obtener imagen de artista por nombre y tipo
export const getArtistImage = (artistName: string, artistType: 'international' | 'national' | 'tamaulipecos'): string => {
  let imageMap: { [key: string]: string }
  
  switch (artistType) {
    case 'international':
      imageMap = internationalArtistImages
      break
    case 'national':
      imageMap = nationalArtistImages
      break
    case 'tamaulipecos':
      imageMap = tamaulipecosArtistImages
      break
    default:
      return "/images/img_0.png"
  }
  
  return imageMap[artistName] || "/images/img_0.png"
}

// Función helper para obtener imagen de artista sin especificar tipo (busca en todos)
export const getArtistImageUniversal = (artistName: string): string => {
  // Buscar en internacionales
  if (internationalArtistImages[artistName]) {
    return internationalArtistImages[artistName]
  }
  
  // Buscar en nacionales
  if (nationalArtistImages[artistName]) {
    return nationalArtistImages[artistName]
  }
  
  // Buscar en tamaulipecos
  if (tamaulipecosArtistImages[artistName]) {
    return tamaulipecosArtistImages[artistName]
  }
  
  // Imagen por defecto
  return "/images/miss.png"
}
